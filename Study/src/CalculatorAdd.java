public class CalculatorAdd
{
    double add(int a, int b) {
        int resultAdd = a + b;
        return resultAdd;
    }
    double add(int a, int b, int c) {
        int resultAdd = a + b + c;
        return resultAdd;
    }
    double add(double a, double b, double c) {
        double resultAdd = a + b + c;
        return resultAdd;
    }

}
